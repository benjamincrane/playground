from inverse import *

class TestInverseFinder:
    def test_adjoints(self):
        assert Matrix([[24,-12,-2],[5,3,-5],[-4,2,4]]) == AdjointFinder([[1,2,3],[0,4,5],[1,0,6]]).adjoint()
        assert Matrix([[6,16,-4, -20],[-16,4,20,16],[56,-28,-28,-56],[-38, 20, 16, 52]]) == AdjointFinder([[2,4,6,6],[3,1,2,3],[1,5,2,1],[0,1,3,4]]).adjoint()
        
    def test_inverse(self):
        assert Matrix([[1.0909090909090908, -0.54545454545454541, -0.090909090909090912], [0.22727272727272727, 0.13636363636363635, -0.22727272727272727], [-0.18181818181818182, 0.090909090909090912, 0.18181818181818182]]) == InverseFinder().inverse([[1,2,3],[0,4,5],[1,0,6]])
        assert  [[0.10714285714285714, 0.2857142857142857, -0.071428571428571425, -0.35714285714285715], [-0.2857142857142857, 0.071428571428571425, 0.35714285714285715, 0.2857142857142857], [1.0, -0.5, -0.5, -1.0], [-0.6785714285714286, 0.35714285714285715, 0.2857142857142857, 0.9285714285714286]] == InverseFinder().inverse([[2,4,6,6],[3,1,2,3],[1,5,2,1],[0,1,3,4]])
    
